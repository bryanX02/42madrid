#include <stdio.h>

int	ft_isalnum(int c)
{
	int	boolean;

	boolean = 0;
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122) || (c >= 48 && c <= 57))
		boolean = 1;
	return (boolean);
}
