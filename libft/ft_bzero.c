#include <stdlib.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	ite;
	char	*c;

	if (s)
	{
		ite = 0;
		while (ite < n)
		{
			c = s + ite;
			*c = 0;
			ite++;
		}
	}
}
